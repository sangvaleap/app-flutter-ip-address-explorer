/// The `AppAsset` class holds the paths to various asset files used in the application.
class AppAsset {
  /// Private constructor to prevent the instantiation of this class.
  AppAsset._();

  /// The path to the application logo image file.
  static const String logo = 'assets/images/logo.jpeg';

  /// The path to the background image file.
  static const String background = 'assets/images/background.jpeg';

  /// The path to the arrow icon SVG file.
  static const String arrowIcon = 'assets/icons/icon-arrow.svg';

  /// The path to the location icon SVG file.
  static const String locationIcon = 'assets/icons/icon-location.svg';

  /// The path to the settiong icon SVG file.
  static const String settingIcon = 'assets/icons/icon-setting.svg';

  /// The path to the google map image file.
  static const String googleMap = 'assets/images/google-map.jpg';

  /// The path to the open street map image file.
  static const String openStreetMap = 'assets/images/open-street-map.jpeg';

  /// The path to the light mode image file.
  static const String lightMode = 'assets/images/light-mode.png';

  /// The path to the dark mode image file.
  static const String darkMode = 'assets/images/dark-mode.png';
}
